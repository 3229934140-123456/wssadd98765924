export function getAnomaliesByVehicle(vehicleId: string): Anomaly[] {
  return mockAnomalies.filter((a) => a.vehicleId === vehicleId);
}

export function getHandoverRecords(anomalyId: string): HandoverRecord[] {
  return mockHandoverRecords.filter((r) => r.anomalyId === anomalyId).sort(
    (a, b) => new Date(b.handleTime).getTime() - new Date(a.handleTime).getTime()
  );
}
